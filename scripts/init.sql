CREATE DATABASE IF NOT EXISTS gerenciamento_veiculos;
USE gerenciamento_veiculos;

CREATE TABLE tb_veiculos (
    cd_veiculo BIGINT PRIMARY KEY AUTO_INCREMENT,
    ds_modelo VARCHAR(255) NOT NULL,
    ds_fabricante VARCHAR(255) NOT NULL,
    nr_ano INT NOT NULL,
    vl_preco DECIMAL(19, 2) NOT NULL,
    tp_veiculo ENUM('CARRO', 'MOTO') NOT NULL,
    ds_cor VARCHAR(255),
    dt_created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    dt_updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON
    UPDATE
    CURRENT_TIMESTAMP,
    INDEX idx_tipo (tp_veiculo),
    INDEX idx_modelo (ds_modelo),
    INDEX idx_fabricante (ds_fabricante),
    INDEX idx_ano (nr_ano),
    INDEX idx_cor (ds_cor)
);

CREATE TABLE tb_carros (
    cd_carro BIGINT PRIMARY KEY,
    qt_portas INT NOT NULL,
    tp_combustivel ENUM('GASOLINA', 'ETANOL', 'DIESEL', 'FLEX') NOT NULL,
    FOREIGN KEY (cd_carro) REFERENCES tb_veiculos(cd_veiculo) ON DELETE CASCADE
);

CREATE TABLE tb_motos (
    cd_moto BIGINT PRIMARY KEY,
    qt_cilindrada INT NOT NULL,
    FOREIGN KEY (cd_moto) REFERENCES tb_veiculos(cd_veiculo) ON DELETE CASCADE
);

